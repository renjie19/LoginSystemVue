class Employee{
    _id = 0;
    _name;
    _age;
    _address;
    _position;

    get id() {
        return this._id;
    }

    set id(value) {
        this._id = value;
    }

    get name() {
        return this._name;
    }

    set name(value) {
        this._name = value;
    }

    get age() {
        return this._age;
    }

    set age(value) {
        this._age = value;
    }

    get address() {
        return this._address;
    }

    set address(value) {
        this._address = value;
    }

    get position() {
        return this._position;
    }

    set position(value) {
        this._position = value;
    }
}
